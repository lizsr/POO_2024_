"""   

  Las tuplas se utilizan para almacenar varios elementos en una sola variable.

Tuple es uno de los 4 tipos de datos integrados en Python que se utilizan 
para almacenar colecciones de datos; los otros 3 son List , Set y Dictionary , 
todos con diferentes calidades y usos.

   Una tupla es una colección ordenada e inmutable .

   Las tuplas se escriben entre paréntesis.


"""

tupla1 = ("apple", "banana", "cherry")
print(tupla1)


tupla2 = ("abc", 34, True, 40, "male")
print(tupla2)


